    private void txtBsKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBsKeyTyped
        char c = evt.getKeyChar();
        if((Character.isDigit(c))||(c==KeyEvent.VK_PERIOD)||(c==KeyEvent.VK_DECIMAL)||(c==KeyEvent.VK_COMMA)||(c==KeyEvent.VK_BACK_SPACE)){
            if(c==KeyEvent.VK_PERIOD) { 
                String s = txtBs.getText();
                int dot = s.indexOf('.');
                if( dot != -1){
                    getToolkit().beep();
                    evt.consume();
                }
            }
        } else {
            getToolkit().beep();
            evt.consume();
        }
	
        if (txtBs.getText().isEmpty()) {
            txtBs.setText("0.00");
            return;
        }
        
        String formatStr = txtBs.getText().trim();
        for (int i = 0; i<formatStr.length(); i++) {
            if (formatStr.charAt(i) == ',') {
                String precioNuevo = formatStr.replace(",", ".");
                if (new Tools().isDouble(formatStr)) {
                    bolivares = Double.valueOf(precioNuevo);
                }
            }
        }
    }//GEN-LAST:event_txtBsKeyTyped

